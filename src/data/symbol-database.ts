// Create a map for quick lookup by symbol
export const SYMBOL_MAP = new Map<string, Symbol>(
  ALL_SYMBOLS.map(sym => [sym.symbol, sym])
);

// Export function to search symbols
export function searchSymbols(query: string, limit = 10): Symbol[] {
  if (!query || query.length === 0) {
    // Return first few if no query
    return ALL_SYMBOLS.slice(0, limit);
  }

  const upperQuery = query.toUpperCase();
  const lowerQuery = query.toLowerCase();

  // First, exact matches
  const exactMatch = ALL_SYMBOLS.filter(s => s.symbol === upperQuery);

  // Then, symbol starts with query
  const symbolStartsWith = ALL_SYMBOLS.filter(s =>
    s.symbol.startsWith(upperQuery) && s.symbol !== upperQuery
  );

  // Then, name contains query
  const nameContains = ALL_SYMBOLS.filter(s => {
    if (exactMatch.includes(s) || symbolStartsWith.includes(s)) return false;
    return s.name.toLowerCase().includes(lowerQuery);
  });

  // Combine results
  const results = [...exactMatch, ...symbolStartsWith, ...nameContains];

  // Remove duplicates and limit
  const seen = new Set<string>();
  return results.filter(s => {
    if (seen.has(s.symbol)) return false;
    seen.add(s.symbol);
    return true;
  }).slice(0, limit);
}

// Get symbol by symbol code
export function getSymbol(symbol: string): Symbol | undefined {
  return SYMBOL_MAP.get(symbol.toUpperCase());
}